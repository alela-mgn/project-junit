package project;

public class Operations {

    public int sum(int num1, int num2) {
        int result = num1 + num2;
        return result;

    }

    public int minus(int num1, int num2) {
        int result = num1 - num2;
        return result;
    }

    public int multiply(int num1, int num2) {
        int result = num1 * num2;
        return result;
    }

    public double division(int num1, int num2) {
        double result;
        result = num1 / num2;
        return result;
    }
}


